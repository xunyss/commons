package io.xunyss.commons.lang;

/**
 * Constant Regular expressions.
 *
 * @author XUNYSS
 */
public class RegularExpressions {
	
	/**
	 * A space.
	 */
	public static final String DELIMITER_SPACE = "\\s";
	
	/**
	 * Spaces.
	 */
	public static final String DELIMITER_SPACES = "\\s+";
	
	/**
	 * Commas or Spaces.
	 */
	public static final String DELIMITER_COMMA_SPACE = "[,\\s]+";
}
